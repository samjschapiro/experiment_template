"""
Generate js/stimuli-data.js from an upstream source.

This template is domain-agnostic: edit `load_items()` and `RATING_DIMENSIONS`
below to match your study. The factory script clones this whole template
directory once per (condition / model / variant) and re-runs this script.

Output schema (see js/stimuli-data.example.js for the precise shape):

    window.STIMULI_DATA = [
        { id: <str>, condition: <str>, text: <str>, prompt?: <str>, ... },
        ...
    ];
    window.RATING_DIMENSIONS = [
        { key: <str>, prompt: <str> },
        ...
    ];

Usage:
    python prepare_stimuli.py [--input PATH] [--seed N]
"""

import argparse
import json
import random
from pathlib import Path


# ── EDIT: rating dimensions for your study ─────────────────────────────────
RATING_DIMENSIONS = [
    {"key": "dimension_1", "prompt": "How would you rate this on dimension 1?"},
    {"key": "dimension_2", "prompt": "How would you rate this on dimension 2?"},
    {"key": "dimension_3", "prompt": "How would you rate this on dimension 3?"},
]

# ── EDIT: design parameters ────────────────────────────────────────────────
N_STIMULI_PER_CONDITION = 20    # → 40 total stimuli for a 2-condition study
TOTAL_SLOTS = 40                # must match backend's TOTAL_SLOTS
DEFAULT_SEED = 42


def load_items(input_path: Path) -> list[dict]:
    """
    Read your upstream data and return a list of dicts, one per candidate
    stimulus. Required keys per dict:
        - 'condition': condition tag (e.g. 'A', 'B', 'baseline', 'treatment')
        - 'text'     : what the participant will see
    Optional keys:
        - 'prompt'        : context shown above the stimulus
        - 'upstream_meta' : any pass-through metadata

    Replace the body below with whatever loader matches your source format.
    """
    raise NotImplementedError(
        f"Edit load_items() in {__file__} to read your upstream source "
        f"({input_path}) and return a list of candidate stimulus dicts."
    )


def balance_stimuli(items: list[dict], n_per_condition: int, seed: int) -> list[dict]:
    """Take a balanced random subset, n_per_condition items per condition."""
    random.seed(seed)
    by_condition: dict[str, list[dict]] = {}
    for item in items:
        by_condition.setdefault(item["condition"], []).append(item)

    selected: list[dict] = []
    for cond, pool in by_condition.items():
        if len(pool) < n_per_condition:
            raise ValueError(
                f"Condition {cond!r} has only {len(pool)} items but "
                f"{n_per_condition} requested."
            )
        selected.extend(random.sample(pool, n_per_condition))

    random.shuffle(selected)
    for i, s in enumerate(selected):
        s["id"] = f"s{i:04d}"
    return selected


def write_bundle(stimuli: list[dict], output_path: Path) -> None:
    """Emit a JS file that the frontend can load via <script src=...>."""
    content = (
        "// Auto-generated by prepare_stimuli.py — do not edit by hand.\n"
        "window.STIMULI_DATA = "
        + json.dumps(stimuli, indent=2)
        + ";\n\n"
        "window.RATING_DIMENSIONS = "
        + json.dumps(RATING_DIMENSIONS, indent=2)
        + ";\n"
    )
    output_path.write_text(content)
    print(f"Wrote {len(stimuli)} stimuli to {output_path}")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", type=Path, required=True,
                        help="Upstream data file (CSV, JSON, etc.).")
    parser.add_argument("--output", type=Path,
                        default=Path(__file__).parent / "js" / "stimuli-data.js")
    parser.add_argument("--seed", type=int, default=DEFAULT_SEED)
    parser.add_argument("--n-per-condition", type=int,
                        default=N_STIMULI_PER_CONDITION)
    args = parser.parse_args()

    items = load_items(args.input)
    stimuli = balance_stimuli(items, args.n_per_condition, args.seed)

    expected_total = args.n_per_condition * len({s["condition"] for s in stimuli})
    if len(stimuli) != expected_total:
        print(f"WARNING: produced {len(stimuli)} stimuli (expected {expected_total}).")
    if expected_total != TOTAL_SLOTS:
        print(f"WARNING: TOTAL_SLOTS ({TOTAL_SLOTS}) != total stimuli "
              f"({expected_total}). Frontend slot logic assumes they match.")

    write_bundle(stimuli, args.output)


if __name__ == "__main__":
    main()
